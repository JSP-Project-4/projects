package com.model;

public class FaqDTO {
	private int faq_num;
	private String faq_title;
	private String faq_cont;
	private int faq_hit;
	private String faq_date;
	
	public int getFaq_num() {
		return faq_num;
	}
	public void setFaq_num(int faq_num) {
		this.faq_num = faq_num;
	}
	public String getFaq_title() {
		return faq_title;
	}
	public void setFaq_title(String faq_title) {
		this.faq_title = faq_title;
	}
	public String getFaq_cont() {
		return faq_cont;
	}
	public void setFaq_cont(String faq_cont) {
		this.faq_cont = faq_cont;
	}
	public int getFaq_hit() {
		return faq_hit;
	}
	public void setFaq_hit(int faq_hit) {
		this.faq_hit = faq_hit;
	}
	public String getFaq_date() {
		return faq_date;
	}
	public void setFaq_date(String faq_date) {
		this.faq_date = faq_date;
	}
	
	
}


